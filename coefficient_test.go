func TestFint_rshEven(t *testing.T) {
	cases := []struct {
		coef  fint
		shift int
		want  fint
	}{
		{20, 1, 2},
		{18, 1, 2},
		{15, 1, 2},
		{12, 1, 1},
		{10, 1, 1},
		{8, 1, 1},
		{5, 1, 0},
		{2, 1, 0},
		{9999999999999999999, 19, 1},
	}
	for _, c := range cases {
		got := c.coef.rshEven(c.shift)
		if got != c.want {
			t.Errorf("%v.rshEven(%v) = %v, want %v", c.coef, c.shift, got, c.want)
		}
	}
}

